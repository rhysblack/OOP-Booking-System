    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        //String each line
        String line;

        //array of each record from the list
        //.getModel().getSize() will count the amount of record currently in the list
        String[] records = new String[lstStock.getModel().getSize()];

        // get values from textbox
        String idNum = txtIDNum.getText().strip();
        String name = txtName.getText().strip();
        String quantity = txtQuantity1.getText().strip();

        //check if they are valid
        //create an error boolean that will be used to check if
        //an entry should be written into the text file or not
        boolean error = false;

        //check if any fields are empty
        if(idNum.strip().equals("") || name.strip().equals("") ||
            name.strip().equals("")){
            error = true;
            JOptionPane.showMessageDialog(null,"Please fill in all fields.","Error",0);

            //check if name is too long
        }else if(name.length() > 50){
            error = true;
            JOptionPane.showMessageDialog(null,"Name is too long.","Error",0);
            txtName.grabFocus();

        }else if(!isNumeric(quantity)){
            error = true;
            JOptionPane.showMessageDialog(null,"Quantity must be a number","Error",0);
            txtQuantity1.grabFocus();
        }

        //only run if there are no errors
        if(!error){
            //confirmation message
            //this will bring up a confirmation box asking yes or no
            //yes will return 0
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(this, "Are you sure you would like to update the record for:\nItem ID number " + idNum, "Update Confirmation", dialogButton);
            if(dialogResult == 0) {
                //continue with edit if yes
                //create a string for all of the info in a line
                String editedRecord = (idNum + ", " + name + ", " +
                    quantity);

                //get all records in list and replace the edited one
                for(int i = 0; i < records.length; i++){
                    //check if the record starts with the stock items id the user entered
                    //get the component at i and check if it starts with the id number
                    if(lstStock.getModel().getElementAt(i).startsWith(idNum)){
                        records[i] = editedRecord;

                        //get record that is currently in the list if the id number is different
                    }else{
                        records[i] = lstStock.getModel().getElementAt(i);
                    }
                }

                //debug code to see contents of list
                for(int i = 0; i < records.length; i++){
                    System.out.println("DEBUG: " + records[i]);
                }

                //rewrite file with array containing edited record
                try {
                    //create instance of the file writer with the chosen file path
                    FileWriter fw = new FileWriter("storage\\stock.txt");

                    //buffered writer for new line
                    BufferedWriter bw = new BufferedWriter(fw);

                    //loop through array
                    for(int i = 0; i < records.length; i++){
                        //write record from array
                        bw.write(records[i]);
                        //take new line
                        bw.newLine();
                    }

                    //close file
                    fw.flush();
                    bw.flush();

                } catch (IOException ex) {
                    //show error window
                    JOptionPane.showMessageDialog(null,"There was an error writing to the file.\n" + ex,"Error",0);
                }

                //show success mesage
                JOptionPane.showMessageDialog(null,"Edit successfully made.","Success",1);

                //clear fields
                lstStock.clearSelection();

                txtName.setText("");
                txtQuantity1.setText("");
                txtIDNum.setText("");

                //reload customer info to list
                loadList();
                
                //edit the data for the current stock objects
                //insert data into a new instance of the stock class
                appData.currentStock.setIdNum(Integer.valueOf(idNum));
                appData.currentStock.setQuantity(Integer.valueOf(quantity));
                appData.currentStock.setName(name);

                //debug code
                System.out.println("DEBUG: " + appData.currentStock.getIdNum());
                System.out.println("DEBUG: " + appData.currentStock.getQuantity());
                System.out.println("DEBUG: " + appData.currentStock.getName());
            } else {
                //do nothing
            }
            
            

        }

    }//GEN-LAST:event_btnUpdateActionPerformed

