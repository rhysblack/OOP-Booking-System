    /**Reads through the stock.txt file and adds each line to a list model
     * lstCustomers is then set to the list model so that it will have the customer info
     * @author R.BLack
     */
    public void loadList(){
        //line variable used to read each line in list
        String line;
        
        //create new list model
        DefaultListModel listModel = new javax.swing.DefaultListModel();
        
        try {
            //create new reader
            FileReader fr = new FileReader("storage\\stock.txt");
            
            //create buffer reader to extend reader
            BufferedReader br = new BufferedReader(fr);
            
            //read until empty line
            while ((line = br.readLine()) != null) {
                //add line to list model
                listModel.addElement(line);
            }
            
            //close file
            br.close();
            fr.close();
            
        } catch (FileNotFoundException fnfEx) {
            //show error window
            JOptionPane.showMessageDialog(null,"There was an error reading the file.\n" + fnfEx,"Error",0);
            
        } catch (IOException ex) {
            //show error window
            JOptionPane.showMessageDialog(null,"There was an error reading the file.\n" + ex,"Error",0);
            
        }
        
        //set the lstCustomers list to the list model with all customer info
        lstStock.setModel(listModel);
    }
    
    /**This method returns true or false if string is numeric
     * @param str this is the string that will be checked
     * @author R.BLack
     */
    public static boolean isNumeric(String str) {
        return str != null && str.matches("[-+]?\\d*\\.?\\d+");
    }

